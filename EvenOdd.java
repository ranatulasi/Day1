import java.util.*;
class EvenOdd
{
public static void main(String... args)
{
System.out.println("Enter a number");
Scanner S=new Scanner(System.in);
int a=S.nextInt();
if(a%2==0)
{
System.out.println("Given number is even");
}
else
{
System.out.println("Given number is odd ");
}
}
}
